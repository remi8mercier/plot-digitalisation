# ------------------------------------------------------------
# Load metrics from ONE comparison_metrics.json
# ------------------------------------------------------------
def load_case_metrics(metrics_path: Path) -> Dict | None:
    """Load metrics from a single comparison_metrics.json file.
    Returns a dict with keys: case_id, success, overall_nmae, l1_scaled_by_gt_yrange
    """
    try:
        with metrics_path.open("r", encoding="utf-8") as f:
            metrics = json.load(f)
    except Exception as exc:
        print(f"[warn] Failed to read {metrics_path}: {exc}")
        return None

    return {
        "case_id": metrics_path.parent.name,
        "success": bool(metrics.get("success", True)),
        "overall_nmae": metrics.get("overall_nmae"),
        "l1_scaled_by_gt_yrange": metrics.get("l1_scaled_by_gt_yrange"),
    }


# ------------------------------------------------------------
# Recursively collect ALL cases under root
# ------------------------------------------------------------
def collect_cases_recursive(root: Path) -> pd.DataFrame:
    """Recursively search for comparison_metrics.json files under root and collect their metrics into a DataFrame."""
    rows: List[Dict] = []

    for metrics_path in root.rglob("comparison_metrics.json"):
        case = load_case_metrics(metrics_path)
        if case is not None:
            rows.append(case)

    if not rows:
        raise ValueError(f"No comparison_metrics.json found under {root}")

    return pd.DataFrame(rows)


# ------------------------------------------------------------
# Compute stats (mean over successful figures only)
# ------------------------------------------------------------
def compute_stats(df: pd.DataFrame, case_ids: List[str]) -> Dict:
    """Compute stats for a subset of cases identified by case_ids."""
    sub = df[df["case_id"].isin(case_ids)]
    n_total = len(sub)

    if n_total == 0:
        return {
            "# Figures": 0,
            "# Success": 0,
            "# Failures": 0,
            "Failure rate": math.nan,
            "Mean NMAE": math.nan,
            "Mean Norm L1": math.nan,
        }

    # Failures = missing metrics
    is_failure = (
        sub["overall_nmae"].isna()
        | sub["l1_scaled_by_gt_yrange"].isna()
    )

    n_fail = int(is_failure.sum())
    n_ok = n_total - n_fail
    failure_rate = n_fail / n_total

    ok = sub[~is_failure]

    mean_nmae = np.mean(ok["overall_nmae"]) if n_ok > 0 else math.nan
    mean_l1 = np.mean(ok["l1_scaled_by_gt_yrange"]) if n_ok > 0 else math.nan
    median_nmae = np.median(ok["overall_nmae"]) if n_ok > 0 else math.nan
    median_l1 = np.median(ok["l1_scaled_by_gt_yrange"]) if n_ok > 0 else math.nan
    return {
        "# Figures": n_total,
        "# Success": n_ok,
        "# Failures": n_fail,
        "Failure rate": failure_rate,
        "Mean NMAE": mean_nmae,
        "Median NMAE": median_nmae,
        "Mean Norm L1": mean_l1,
        "Median Norm L1": median_l1,
    }


# ------------------------------------------------------------
# Build summary table (Overall + by chart type)
# ------------------------------------------------------------
def build_summary_table(df: pd.DataFrame) -> pd.DataFrame:
    """Build a summary table with overall stats and stats by chart type."""
    rows = []

    all_ids = NORMAL_NAMES + KM_NAMES + CATEGORICAL_NAMES

    rows.append({"Chart type": "Overall", **compute_stats(df, all_ids)})
    rows.append({"Chart type": "Normal", **compute_stats(df, NORMAL_NAMES)})
    rows.append({"Chart type": "KM", **compute_stats(df, KM_NAMES)})
    rows.append(
        {"Chart type": "Categorical", **compute_stats(df, CATEGORICAL_NAMES)}
    )

    return pd.DataFrame(rows)


# ------------------------------------------------------------
# Main
# ------------------------------------------------------------
def main() -> None:
    root = Path(INPUT_FILE_PATH)

    if not root.exists():
        raise FileNotFoundError(f"Input path not found: {root}")

    df = collect_cases_recursive(root)

    table = build_summary_table(df)

    print("\n=== Summary by chart type ===")
    print(table.to_string(index=False))


if __name__ == "__main__":
    main()
